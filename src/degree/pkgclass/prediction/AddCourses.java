    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int thisYear = Integer.parseInt((String)this.year.getSelectedItem());
        int smr = Integer.parseInt((String)this.sem.getSelectedItem());
        
        for(Year y: this.currentStudent.getYears()){
            if(y.getYear() == thisYear){
                int avSem = 0;
                
                if(y.getSemesters().size()<=2){
                    for(Semester s: y.getSemesters()){
                        avSem = s.getSemesterNumber();
                    }
                    if(smr != avSem){
                        Semester sm = new Semester();
                        sm.setSemesterNumber(smr);
                    for(int i = 0; i<this.table.getRowCount(); i++){
                        String cc = (String)this.table.getValueAt(i, 0);
                        String cn = (String)this.table.getValueAt(i, 1);
                        String cm = (String)this.table.getValueAt(i, 2);

                        if(cc != null || cn != null || cm!= null){
                            Course cs = new Course();
                            cs.setCourseCode(cc);
                            cs.setCourseNarration(cn);
                            cs.setCourseMark(Float.parseFloat(cm));
                            sm.addCourse(cs);
                        }
                    }
                    y.addSemester(sm);
                    }
                }
            }
        }
        
        new AddUserInfo().addCourses(currentStudent);
        setVisible(false);
        dispose();
    }//GEN-LAST:event_jButton2ActionPerformed

