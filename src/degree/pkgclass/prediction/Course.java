package degree.pkgclass.prediction;

public class Course {
    private String courseCode;
    private String courseNarration;
    private float courseMark;

    public String getCourseCode() {
        return courseCode;
    }

    public void setCourseCode(String courseCode) {
        this.courseCode = courseCode;
    }

    public String getCourseNarration() {
        return courseNarration;
    }

    public void setCourseNarration(String courseNarration) {
        this.courseNarration = courseNarration;
    }

    public float getCourseMark() {
        return courseMark;
    }

    public void setCourseMark(float courseMark) {
        this.courseMark = courseMark;
    }
    
}
